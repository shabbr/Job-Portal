<script>
    function search() {
        // Get the search input value
        var searchText = document.getElementById("searchInput").value.toLowerCase();
        // Get all elements where you want to search
        var elements = document.querySelectorAll("p, h1, h2, h3, h4, h5, h6, span, div, li, a");
        var searchResults = [];

        // Loop through all elements
        elements.forEach(function(element) {
            // Check if the element contains the search text
            if (element.innerText.toLowerCase().includes(searchText)) {
                // If it does, add it to the search results
                searchResults.push(element.outerHTML);
            }
        });

        // Display the search results
        var resultsContainer = document.getElementById("searchResults");
        resultsContainer.innerHTML = searchResults.length > 0 ? searchResults.join("") : "No results found.";
    }
    </script>
